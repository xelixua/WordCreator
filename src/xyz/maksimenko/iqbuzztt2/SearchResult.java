package xyz.maksimenko.iqbuzztt2;

public class SearchResult {
	private String preWord;
	private int targetPosition;
	private String postWord;
	
	public String getPreWord() {
		return preWord;
	}
	public void setPreWord(String preWord) {
		this.preWord = preWord;
	}
	public int getTargetPosition() {
		return targetPosition;
	}
	public void setTargetPosition(int targetPosition) {
		this.targetPosition = targetPosition;
	}
	public String getPostWord() {
		return postWord;
	}
	public void setPostWord(String postWord) {
		this.postWord = postWord;
	}
	
	
}
